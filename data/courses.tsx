export const getAllAnnouncements = () => {
    let arr:any = []
    allcourses.forEach((item:any)=>{
        item.announcements.forEach((item:any)=>{
            arr.push(item)
        })
    })
    return arr
}

export const getRecentAnnouncements = () => {
    const arr = getAllAnnouncements()
    return arr.filter((item:any)=>item.daysago < 14)
    .sort((a:any,b:any)=>a.daysago-b.daysago)
}

